package dailyassignments;

public class Assign_13_14_15Printonetoten {

	public static void main(String[] args) {
		// print 1 to 10 using FOR Loop

		for (int i = 1; i <= 10; i++) {
			System.out.println(i);
		}
//a program to print -10 to 10 using FOR Loop

		for (int i = -10; i <= 10; i++) {
			System.out.println(i);
		}

		// to print -5 to -15 using FOR Loop

		for (int i = -5; i >= -15; i--) {
			System.out.println(i);
		}
	}

}
